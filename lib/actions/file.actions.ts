const handleError = (error: unknown, message: string) => {
  console.log(error, message);
  throw error;
};

export const uploadFile = async ({
  file,
  ownerId,
  accountId,
  path,
}: UploadFileProps) => {// fetching this file eslint wont recognise so we should set the rules for this file
  const { storage, databases } = await createAdminClient();

  try {
    // Convert file to Appwrite InputFile
    const inputFile = InputFile.fromBuffer(file, file.name);

    // Upload file to Appwrite storage
    const bucketFile = await storage.createFile(
      appwriteConfig.bucketId,
      ID.unique(),
      inputFile,
    );

    // Construct file URL
    const fileUrl = constructFileUrl(bucketFile.$id);

    // Validate the constructed URL
    const isValidUrl = (url: string) => {
      try {
        new URL(url);
        return true;
      } catch {
        return false;
      }
    };

    if (!isValidUrl(fileUrl)) {
      // Delete file from storage if URL is invalid
      await storage.deleteFile(appwriteConfig.bucketId, bucketFile.$id);
      throw new Error(`Invalid URL format: ${fileUrl}`);
    }

    // Create a document for the uploaded file
    const fileDocument = {
      type: getFileType(bucketFile.name).type,
      name: bucketFile.name,
      url: fileUrl,
      extension: getFileType(bucketFile.name).extension,
      size: bucketFile.sizeOriginal,
      owner: ownerId,
      accountId,
      users: [],
      bucketFileId: bucketFile.$id,
    };

    const newFile = await databases.createDocument(
      appwriteConfig.databaseId,
      appwriteConfig.filesCollectionId,
      ID.unique(),
      fileDocument,
    );

    // Revalidate path for caching purposes
    revalidatePath(path);

    return parseStringify(newFile);
  } catch (error) {
    handleError(error, "Failed to upload file");
  }
};


const createQueries = (  //helper function for the down main function 
  currentUser: Models.Document,
  types: string,
  searchText : string,
  sort : string,
  limit?: number,
) => {
  const queries = [
    Query.or([
      Query.equal("owner", [currentUser.$id]),
      Query.contains("users", [currentUser.email]),// this we cant say equal because both cont be same
    ]),
  ];

  if (types.length > 0) queries.push(Query.equal("type", types));
  if (searchText) queries.push(Query.contains("name", searchText));
  if (limit) queries.push(Query.limit(limit));

  if (sort) {
    const [sortBy, orderBy] = sort.split("-");

    queries.push(
      orderBy === "asc" ? Query.orderAsc(sortBy) : Query.orderDesc(sortBy),
    );
  }

  console.log(queries);

  return queries;
};

export const getFiles = async ({ types = [], searchText = "", sort = "$createdAt-desc", limit,}: GetFilesProps) => {
  const { databases } = await createAdminClient();

  try {
    const currentUser = await getCurrentUser();

    if (!currentUser) throw new Error("User not found");

    const queries = createQueries(currentUser, types, searchText, sort, limit);

    console.log({ currentUser, queries});

    const files = await databases.listDocuments(// database and its collect
      appwriteConfig.databaseId,
      appwriteConfig.filesCollectionId,
      queries,
    );

    console.log({ files });
    return parseStringify(files);
  } catch (error) {
    handleError(error, "Failed to get files");
  }
};

export const renameFile = async ({
  fileId,
  name,
  extension,
  path,
}: RenameFileProps) => {
  const { databases } = await createAdminClient(); // access tp databases

  try {
    const newName = `${name}.${extension}`; // new file name
    const updatedFile = await databases.updateDocument( // rendering meta data 
      appwriteConfig.databaseId,
      appwriteConfig.filesCollectionId,
      fileId,
      {
        name: newName,
      },
    );

    revalidatePath(path);
    return parseStringify(updatedFile);
  } catch (error) {
    handleError(error, "Failed to rename file");
  }
};

